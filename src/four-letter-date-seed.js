// This ridiculous method generates a different four letter word each hour, selected from a set
// of words that have been screened of homophones and difficult spellings
const fourLetterDateSeed = () => {
    var date = new Date();
    var dateToTheHour = '' + date.getUTCFullYear() + date.getUTCDay() + (date.getUTCHours());
    console.log(dateToTheHour);

    const seedrandom = require('seedrandom');
    var rng = seedrandom(dateToTheHour);

    var word = fourLetterWordList[Math.floor(rng() * fourLetterWordList.length)];

    return word;
}

